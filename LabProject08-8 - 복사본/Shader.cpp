void CBillBoardShader::BuildObjects(ID3D12Device * pd3dDevice, ID3D12GraphicsCommandList * pd3dCommandList, void * pContext)
{
	CHeightMapTerrain *pTerrain = (CHeightMapTerrain *)pContext;
	
	m_pMaterial = new CMaterial();
	CTexture *pTexture = new CTexture(6, RESOURCE_TEXTURE2D_ARRAY, 0);
	pTexture->LoadTextureFromFile(pd3dDevice, pd3dCommandList, L"Resource/Grasses/FlowerBush1.dds", 0);
	pTexture->LoadTextureFromFile(pd3dDevice, pd3dCommandList, L"Resource/Grasses/Cactus3.dds", 1);
	pTexture->LoadTextureFromFile(pd3dDevice, pd3dCommandList, L"Resource/Grasses/Cactus4.dds", 2);
	pTexture->LoadTextureFromFile(pd3dDevice, pd3dCommandList, L"Resource/Grasses/Grass29.dds", 3);
	pTexture->LoadTextureFromFile(pd3dDevice, pd3dCommandList, L"Resource/Grasses/Grass03.dds", 4);
	pTexture->LoadTextureFromFile(pd3dDevice, pd3dCommandList, L"Resource/Grasses/Grass05.dds", 5);

	CreateCbvAndSrvDescriptorHeaps(pd3dDevice, pd3dCommandList, 0, 6);
	CreateShaderResourceViews(pd3dDevice, pd3dCommandList, pTexture, 10, false);

	m_pMaterial->SetTexture(pTexture);
	m_pMaterial->SetReflection(0);
	
	m_nTrees = 20000;
	m_nStride = sizeof(CBillBoardVertex);
	m_nVertices = m_nTrees;
	XMFLOAT3 xmf3Position;
	int cxTerrain = pTerrain->GetHeightMapWidth() * pTerrain->GetScale().x;
	int czTerrain = pTerrain->GetHeightMapLength() * pTerrain->GetScale().z;

	CBillBoardVertex *pTreeVertices = new CBillBoardVertex[m_nVertices];
	
	for (int i = 0; i < m_nTrees; ++i)
	{
		float xsize = 50.f + (rand() % 10);
		float ysize = 60.f + (rand() % 10);
		float fxTerrain = xmf3Position.x = rand() % cxTerrain;
		float fzTerrain = xmf3Position.z = rand() % czTerrain;
		if (pTerrain->GetNormal(fxTerrain, fzTerrain).y < 0.7 ||
			pTerrain->GetHeight(fxTerrain, fzTerrain) < 1)
		{
			--i;
			continue;
		}
		xmf3Position.y = pTerrain->GetHeight(fxTerrain, fzTerrain) + (ysize*0.4);
	
		pTreeVertices[i] = CBillBoardVertex(xmf3Position, XMFLOAT2(xsize, ysize));
	}
	
	m_pd3dVertexBuffer = ::CreateBufferResource(
		pd3dDevice,
		pd3dCommandList,
		pTreeVertices,
		m_nStride*m_nVertices,
		D3D12_HEAP_TYPE_DEFAULT,
		D3D12_RESOURCE_STATE_VERTEX_AND_CONSTANT_BUFFER,
		&m_pd3dVertexUploadBuffer
	);
	
	m_d3dVertexBufferView.BufferLocation = m_pd3dVertexBuffer->GetGPUVirtualAddress();
	m_d3dVertexBufferView.StrideInBytes = m_nStride;
	m_d3dVertexBufferView.SizeInBytes = m_nStride*m_nVertices;
}

void CBillBoardShader::Render(ID3D12GraphicsCommandList * pd3dCommandList, CCamera * pCamera)
{
	//if (m_ppd3dPipelineStates) pd3dCommandList->SetPipelineState(m_ppd3dPipelineStates[0]);
	OnPrepareRender(pd3dCommandList);

	m_pMaterial->UpdateShaderVariables(pd3dCommandList);

	pd3dCommandList->IASetPrimitiveTopology(m_d3dPrimitiveTopology);
	pd3dCommandList->IASetVertexBuffers(m_nSlot, 1, &m_d3dVertexBufferView);

	pd3dCommandList->DrawInstanced(m_nVertices, 1, m_nOffset, 0);
}

