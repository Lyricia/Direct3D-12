void CParticleBillBoard::BuildObjects(ID3D12Device * pd3dDevice, ID3D12GraphicsCommandList * pd3dCommandList, void * pContext)
{
	m_pMaterial = new CMaterial();
	CTexture *pTexture = new CTexture(1, RESOURCE_TEXTURE2D_ARRAY, 0);
	pTexture->LoadTextureFromFile(pd3dDevice, pd3dCommandList, L"Resource/Sprite/Explode8.dds", 0);

	CreateCbvAndSrvDescriptorHeaps(pd3dDevice, pd3dCommandList, 0, 1);
	CreateShaderResourceViews(pd3dDevice, pd3dCommandList, pTexture, 11, false);
	
	m_pMaterial->SetTexture(pTexture);
	m_pMaterial->SetReflection(0);

	m_nParticles = 1000;
	m_nStride = sizeof(CParticleBillBoardVertex);
	m_nVertices = m_nParticles;
	
	m_ppVertices = new CParticleBillBoardVertex[m_nParticles];

	for (int i = 0; i < m_nParticles; ++i)
	{
		m_ppVertices[i] = CParticleBillBoardVertex(XMFLOAT3(500.0f, 5000.0f, 500.0f), XMFLOAT2(100, 100), XMFLOAT2(0, 0));
	}

	m_pd3dVertexBuffer = ::CreateBufferResource(
		pd3dDevice,
		pd3dCommandList,
		m_ppVertices,
		m_nStride*m_nVertices,
		D3D12_HEAP_TYPE_UPLOAD,
		D3D12_RESOURCE_STATE_VERTEX_AND_CONSTANT_BUFFER,
		&m_pd3dVertexUploadBuffer
	);

	m_pd3dVertexBuffer->Map(0, NULL, (void **)&m_ppVertices);

	m_d3dVertexBufferView.BufferLocation = m_pd3dVertexBuffer->GetGPUVirtualAddress();
	m_d3dVertexBufferView.StrideInBytes = m_nStride;
	m_d3dVertexBufferView.SizeInBytes = m_nStride*m_nVertices;
}

void CParticleBillBoard::AnimateObjects(float fTimeElapsed)
{
	for (int i = 0; i < 1000; ++i)
	{
		if (m_ActiveParticle[i] == true) {
			m_AnimationTime[i] += fTimeElapsed;
			if (m_AnimationTime[i] > 0.05f)
			{
				m_AnimationTime[i] = 0.f;
				m_ppVertices[i].m_xmf2SpritePos.x++;
				if (m_ppVertices[i].m_xmf2SpritePos.x > 7)
				{
					m_ppVertices[i].m_xmf2SpritePos.x = 0;
					m_ppVertices[i].m_xmf2SpritePos.y++;
				}
				if (m_ppVertices[i].m_xmf2SpritePos.y > 7) 
				{
					m_ppVertices[i].m_xmf2SpritePos.y = 0;
					m_ActiveParticle[i] = false;
					m_ppVertices[i].m_xmf3Position = XMFLOAT3(500.0f, 5000.0f, 500.0f);
				}
			}
			m_ppVertices[i].m_xmf3Position = Vector3::Add(m_ppVertices[i].m_xmf3Position, m_ActiveParticleDirection[i]);
		}
	}
}

void CParticleBillBoard::ActivateParticle(XMFLOAT3 pos)
{
	for (int i = m_ActiveParticleIdx; i < m_ActiveParticleIdx + 20; ++i) {
		m_ActiveParticle[i] = true;
		m_ppVertices[i].m_xmf3Position = pos;
		m_ActiveParticleDirection[i].x = -5.f + float(rand() % 10);
		m_ActiveParticleDirection[i].y = -5.f + float(rand() % 10);
		m_ActiveParticleDirection[i].z = -5.f + float(rand() % 10);
		m_ActiveParticleDirection[i] = Vector3::ScalarProduct(m_ActiveParticleDirection[i], 0.05f, false);
	}
	m_ActiveParticleIdx += 20;
	if (m_ActiveParticleIdx >= m_nParticles)
		m_ActiveParticleIdx = 0;
}

void CParticleBillBoard::Render(ID3D12GraphicsCommandList * pd3dCommandList, CCamera * pCamera)
{
	UpdateShaderVariables(pd3dCommandList);

	OnPrepareRender(pd3dCommandList);

	m_pMaterial->UpdateShaderVariables(pd3dCommandList);

	pd3dCommandList->IASetPrimitiveTopology(m_d3dPrimitiveTopology);
	pd3dCommandList->IASetVertexBuffers(m_nSlot, 1, &m_d3dVertexBufferView);

	pd3dCommandList->DrawInstanced(m_nVertices, 1, m_nOffset, 0);
}

